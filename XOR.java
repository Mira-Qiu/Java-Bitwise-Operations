/*
Bitwise XOR(^): it returns bit by bit XOR of input value, i.e, if corresponding bits are different, it gives 1, else it gives 0.
*/

public class Test {
      public static void main(String[] args){
            System.out.println(5 ^ 3);  //  output : 6
      }
}


